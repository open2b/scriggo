// Package untyped declares types used to represent untyped constant values
// passed to the build functions of Scriggo.
package untyped

// StringConst represents an untyped string constant.
type StringConst string

// BooleanConst represents an untyped boolean constant.
type BooleanConst bool

// NumericConst represents an untyped numeric constant.
type NumericConst string
